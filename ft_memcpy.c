#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, unsigned int n)
{
	char			*cdest;
	char			*csrc;
	unsigned int	i;

	if (!dest && !src)
		return (NULL);
	cdest = (char *)dest;
	csrc = (char *)src;
	i = 0;
	while (i < n)
	{
		cdest[i] = csrc[i];
		i++;
	}
	return (dest);
}
