#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		len;
	int		i;
	char	*new;

	len = ft_strlen(s);
	i = 0;
	new = (char *) malloc(sizeof(char) * len + 1);
	if (new == 0)
		return (new);
	while (i < len)
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
